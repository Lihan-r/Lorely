export function Sidebar({ projectId, projectName }: SidebarProps) {
  const pathname = usePathname();

  const getNavItems = (): NavItem[] => {
    if (!projectId) {
      return [
        {
          label: "Projects",
          href: "/projects",
          icon: (
            <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M3 7v10a2 2 0 002 2h14a2 2 0 002-2V9a2 2 0 00-2-2h-6l-2-2H5a2 2 0 00-2 2z" />
            </svg>
          ),
        },
      ];
    }

    return [
      {
        label: "Overview",
        href: `/projects/${projectId}`,
        icon: (
          <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M4 6a2 2 0 012-2h2a2 2 0 012 2v2a2 2 0 01-2 2H6a2 2 0 01-2-2V6zM14 6a2 2 0 012-2h2a2 2 0 012 2v2a2 2 0 01-2 2h-2a2 2 0 01-2-2V6zM4 16a2 2 0 012-2h2a2 2 0 012 2v2a2 2 0 01-2 2H6a2 2 0 01-2-2v-2zM14 16a2 2 0 012-2h2a2 2 0 012 2v2a2 2 0 01-2 2h-2a2 2 0 01-2-2v-2z" />
          </svg>
        ),
      },
      {
        label: "Web View",
        href: `/projects/${projectId}/web`,
        icon: (
          <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M13.828 10.172a4 4 0 00-5.656 0l-4 4a4 4 0 105.656 5.656l1.102-1.101m-.758-4.899a4 4 0 005.656 0l4-4a4 4 0 00-5.656-5.656l-1.1 1.1" />
          </svg>
        ),
      },
      { label: "Characters", href: `/projects/${projectId}/characters`, icon: entityTypeIcons.CHARACTER, type: "CHARACTER" },
      { label: "Locations", href: `/projects/${projectId}/locations`, icon: entityTypeIcons.LOCATION, type: "LOCATION" },
      { label: "Factions", href: `/projects/${projectId}/factions`, icon: entityTypeIcons.FACTION, type: "FACTION" },
      { label: "Items", href: `/projects/${projectId}/items`, icon: entityTypeIcons.ITEM, type: "ITEM" },
      { label: "Events", href: `/projects/${projectId}/events`, icon: entityTypeIcons.EVENT, type: "EVENT" },
      { label: "Chapters", href: `/projects/${projectId}/chapters`, icon: entityTypeIcons.CHAPTER, type: "CHAPTER" },
      { label: "Concepts", href: `/projects/${projectId}/concepts`, icon: entityTypeIcons.CONCEPT, type: "CONCEPT" },
    ];
  };

  const navItems = getNavItems();

  const isActive = (href: string) => {
    if (href === "/projects") {
      return pathname === "/projects";
    }
    if (href === `/projects/${projectId}`) {
      return pathname === `/projects/${projectId}`;
    }
    return pathname.startsWith(href);
  };

  return (
    <aside className="w-60 h-full bg-paper border-r border-border-light flex flex-col">
      {/* Project Selector / Back to Projects */}
      <div className="p-4 border-b border-border-light">
        {projectId ? (
          <div>
            <Link
              href="/projects"
              className="text-xs text-ink/50 hover:text-ink flex items-center gap-1 mb-1"
            >
              <svg className="w-3 h-3" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15 19l-7-7 7-7" />
              </svg>
              All Projects
            </Link>
            <h2 className="font-semibold text-ink truncate">{projectName || "Project"}</h2>
          </div>
        ) : (
          <h2 className="font-semibold text-ink">Lorely</h2>
        )}
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-2 overflow-y-auto">
        <ul className="space-y-1">
          {navItems.map((item) => (
            <li key={item.href}>
              <Link
                href={item.href}
                className={`flex items-center gap-3 px-3 py-2 rounded-lg text-sm transition-colors ${
                  isActive(item.href)
                    ? "bg-cream text-ink font-medium"
                    : "text-ink/70 hover:bg-cream/50 hover:text-ink"
                }`}
              >
                {item.icon}
                {item.label}
              </Link>
            </li>
          ))}
        </ul>
      </nav>

      {/* Footer */}
      <div className="p-4 border-t border-border-light">
        <Link
          href="/projects"
          className="flex items-center gap-2 text-sm text-ink/60 hover:text-ink"
        >
          <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M10.325 4.317c.426-1.756 2.924-1.756 3.35 0a1.724 1.724 0 002.573 1.066c1.543-.94 3.31.826 2.37 2.37a1.724 1.724 0 001.065 2.572c1.756.426 1.756 2.924 0 3.35a1.724 1.724 0 00-1.066 2.573c.94 1.543-.826 3.31-2.37 2.37a1.724 1.724 0 00-2.572 1.065c-.426 1.756-2.924 1.756-3.35 0a1.724 1.724 0 00-2.573-1.066c-1.543.94-3.31-.826-2.37-2.37a1.724 1.724 0 00-1.065-2.572c-1.756-.426-1.756-2.924 0-3.35a1.724 1.724 0 001.066-2.573c-.94-1.543.826-3.31 2.37-2.37.996.608 2.296.07 2.572-1.065z" />
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M15 12a3 3 0 11-6 0 3 3 0 016 0z" />
          </svg>
          Settings
        </Link>
      </div>
    </aside>
  );
}
